package lt.shmup.main.game.gameobject.transform;

public class TransformHandler {

    private float x, y;

    public TransformHandler(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

}
